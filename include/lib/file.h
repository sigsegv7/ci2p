#ifndef LIB_FILE_H_
#define LIB_FILE_H_

#include <stdint.h>
#include <stddef.h>
#include <stdio.h>

/* Macro to get the 'handle' from a file */
#define FHANDLE(FP) ((FP)->handle)
#define FSIZE(FP) ((FP)->size)

struct file {
    size_t size;
    FILE *handle;
};

int open_file(const char *filename, const char *mode, struct file **res);
void close_file(struct file *fp);

#endif  /* LIB_FILE_H_ */
